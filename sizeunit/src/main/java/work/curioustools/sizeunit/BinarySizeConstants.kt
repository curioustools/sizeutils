package work.curioustools.sizeunit

object BinarySizeConstants {
    const val BITS_IN_ONE_BIT:Long = 1
    const val BITS_IN_ONE_BYTE:Long = 8 * BITS_IN_ONE_BIT
    const val BITS_IN_ONE_KB:Long = 1024 * BITS_IN_ONE_BYTE
    const val BITS_IN_ONE_MB:Long = 1024 * BITS_IN_ONE_KB
    const val BITS_IN_ONE_GB:Long = 1024 * BITS_IN_ONE_MB
    const val BITS_IN_ONE_TB:Long = 1024 * BITS_IN_ONE_GB
    const val BITS_IN_ONE_PB:Long = 1024 * BITS_IN_ONE_TB

}